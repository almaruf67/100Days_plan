alp = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z','a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def caesar(message,shift,fun):
    en_mes = ""
    if fun=='decode':
        shift*=-1
    for letter in message:
        if letter not in alp:
            en_mes+=letter
            continue
        pos = alp.index(letter)+shift
        en_mes+= alp[pos]
    print(f'Here\'s the {fun}d result :\n{en_mes}')

cont = 'yes'

